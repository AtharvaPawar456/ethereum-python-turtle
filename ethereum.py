import turtle
turtle.bgcolor("black")
t=turtle.Turtle()
t.pensize(4)
turtle.speed(5)
t.color("black")
t.begin_fill()
t.fillcolor("purple")
t.rt(90)
t.bk(100)
t.fd(150)
t.lt(90)
t.goto(100,0)
t.goto(0,100)
t.end_fill()###
t.begin_fill()
t.fillcolor("green")
t.goto(-100,0)
t.goto(0,-50)
t.end_fill()###
t.lt(90)
t.fd(150)###
t.begin_fill()
t.fillcolor("red")
t.goto(100,0)
t.goto(0,250)
t.end_fill()###
t.bk(150)###
t.begin_fill()
t.fillcolor("yellow")
t.goto(-100,0)
t.goto(0,250)
t.end_fill()###
t.penup()   
t.bk(310)
t.pendown()###
t.begin_fill()
t.fillcolor("blue")
t.goto(100,-10)
t.goto(0,-140)
t.fd(80)
t.end_fill()###
t.begin_fill()
t.fillcolor("orange")
t.goto(-100,-10)
t.goto(0,-140)
t.end_fill()###
t.fd(500)
t.bk(550)
t.color("yellow")
t.write("ETHEREUM",'false','center',font=('Showcard gothic',20))
t.lt(90)
t.fd(140)
t.penup()
t.bk(1000)
turtle.done()